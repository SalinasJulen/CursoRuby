# Las posiciones de los arrays en Ruby empiezan desde 0, al igual que Java


=begin
  Esta es la estructura básica de un array en Ruby:
    Declaración
    Acceso
    Modificación
    Recorrer
    Borrar
    Ordenar
=end

# Array vacío:
ages = []

# Declaración de un array con valores:
names = ["Julen","Luis","Enara","Lola"]



# Acceder a una posición conreta del array (en este caso la segunda posición):
puts names[2]

# Modificar un valor concreto (en este caso la primera posición):
names[1] = "Oier"

puts names[1]
